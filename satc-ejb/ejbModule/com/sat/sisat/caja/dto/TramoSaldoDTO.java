package com.sat.sisat.caja.dto;

import java.io.Serializable;
public class TramoSaldoDTO implements Serializable {

	
	private static final long serialVersionUID = 9114729860328230778L;
	
	private int tramoId;
	private String tramoDescripcion;
	public int getTramoId() {
		return tramoId;
	}
	public void setTramoId(int tramoId) {
		this.tramoId = tramoId;
	}
	public String getTramoDescripcion() {
		return tramoDescripcion;
	}
	public void setTramoDescripcion(String tramoDescripcion) {
		this.tramoDescripcion = tramoDescripcion;
	}		
}

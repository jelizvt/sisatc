	public TipoRegistroSunarpDTO  getCodigoTipoRegistro (String tipoRegistro)
	{
		TipoRegistroSunarpDTO  retorno=new TipoRegistroSunarpDTO();
		retorno.settipoRegistro(tipoRegistro.trim().toUpperCase()) ;
		
		//Buscamos el código  para el tipo de registro.		
		for (TipoRegistroSunarpDTO a : tipoRegsitros)
		{	
			if (a.gettipoRegistro().equals(retorno.gettipoRegistro()))
			{
				retorno.setCodigo(a.getCodigo());
				break;
			}
		}
		
		return retorno;
			
	}
	
	
	public OficinaSunarpDTO getCodZonaOficina(String oficina)
	{
		
		if (oficina == null) return null;
		
		OficinaSunarpDTO retorno = new OficinaSunarpDTO();
		retorno.setDescripcion(oficina);
		
		//Buscamos los códigos de Zona y Oficina		
		for (OficinaSunarpDTO a: oficinas)
		{	
			
			if (a.getDescripcion().trim().equals(retorno.getDescripcion().trim()))
			{
				//Existe
				retorno.setCodZona(a.getCodZona());
				retorno.setCodOficina(a.getCodOficina());
				break;
				
			}
		}
		
		return retorno;
	}
	


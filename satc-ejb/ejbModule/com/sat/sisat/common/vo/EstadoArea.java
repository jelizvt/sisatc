package com.sat.sisat.common.vo;

import java.io.Serializable;

public class EstadoArea implements Serializable {
	private Boolean areaTerreno;
	private Boolean areaComunTerreno;
	private Boolean areaConstruida;
	private Boolean areaComunConstruida;
	
	public EstadoArea(Boolean areaTerreno,Boolean areaComunTerreno,Boolean areaConstruida,Boolean areaComunConstruida){
		this.areaTerreno=areaTerreno;
		this.areaComunTerreno=areaComunTerreno;
		this.areaConstruida=areaConstruida;
		this.areaComunConstruida=areaComunConstruida;
	}
	
	public Boolean getAreaTerreno() {
		return areaTerreno;
	}
	public void setAreaTerreno(Boolean areaTerreno) {
		this.areaTerreno = areaTerreno;
	}
	public Boolean getAreaComunTerreno() {
		return areaComunTerreno;
	}
	public void setAreaComunTerreno(Boolean areaComunTerreno) {
		this.areaComunTerreno = areaComunTerreno;
	}
	public Boolean getAreaConstruida() {
		return areaConstruida;
	}
	public void setAreaConstruida(Boolean areaConstruida) {
		this.areaConstruida = areaConstruida;
	}
	public Boolean getAreaComunConstruida() {
		return areaComunConstruida;
	}
	public void setAreaComunConstruida(Boolean areaComunConstruida) {
		this.areaComunConstruida = areaComunConstruida;
	}
}

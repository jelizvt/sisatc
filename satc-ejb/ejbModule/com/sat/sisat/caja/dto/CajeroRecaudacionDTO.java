package com.sat.sisat.caja.dto;

import java.io.Serializable;
import java.math.BigDecimal;

public class CajeroRecaudacionDTO implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 5073848005857815949L;

	private String nombreUsuario;	
	
	private Integer aperturaId;
	
	private String estado;
	
	private BigDecimal monto;

	public String getNombreUsuario() {
		return nombreUsuario;
	}

	public void setNombreUsuario(String nombreUsuario) {
		this.nombreUsuario = nombreUsuario;
	}

	public Integer getAperturaId() {
		return aperturaId;
	}

	public void setAperturaId(Integer aperturaId) {
		this.aperturaId = aperturaId;
	}

	public String getEstado() {
		return estado;
	}

	public void setEstado(String estado) {
		this.estado = estado;
	}

	public BigDecimal getMonto() {
		return monto;
	}

	public void setMonto(BigDecimal monto) {
		this.monto = monto;
	}
	
		
}

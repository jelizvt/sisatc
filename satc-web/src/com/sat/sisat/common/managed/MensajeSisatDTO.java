package com.sat.sisat.common.managed;

import java.io.Serializable;

public class MensajeSisatDTO implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7871502770007184350L;

	private String mensaje;
	private int tipoColor;
	private String action;

	public String getMensaje() {
		return mensaje;
	}

	public void setMensaje(String mensaje) {
		this.mensaje = mensaje;
	}

	public int getTipoColor() {
		return tipoColor;
	}

	public void setTipoColor(int tipoColor) {
		this.tipoColor = tipoColor;
	}

	public String getAction() {
		return action;
	}

	public void setAction(String action) {
		this.action = action;
	}

}

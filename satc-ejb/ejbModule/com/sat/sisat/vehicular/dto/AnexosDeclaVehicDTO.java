package com.sat.sisat.vehicular.dto;

import java.io.Serializable;

public class AnexosDeclaVehicDTO implements Serializable{

	private int docSustentatorioId;
	private String descripcionDoc;
	private String glosaDoc;
	private String nomDocAdjunto;
	
	//INICIO ITANTAMANGO
	private Integer contentId;
	
	public Integer getContentId() {
		return contentId;
	}

	public void setContentId(Integer contentId) {
		this.contentId = contentId;
	}
	//FIN ITANTAMANGO

	public AnexosDeclaVehicDTO() {
		super();
	}

	public AnexosDeclaVehicDTO(int docSustentatorioId, String descripcionDoc,
			String glosaDoc, String nomDocAdjunto,Integer contenidoId) {
		super();
		this.docSustentatorioId = docSustentatorioId;
		this.descripcionDoc = descripcionDoc;
		this.glosaDoc = glosaDoc;
		this.nomDocAdjunto = nomDocAdjunto;
		this.contentId = contenidoId;
	}

	public int getDocSustentatorioId() {
		return docSustentatorioId;
	}

	public void setDocSustentatorioId(int docSustentatorioId) {
		this.docSustentatorioId = docSustentatorioId;
	}

	public String getDescripcionDoc() {
		return descripcionDoc;
	}

	public void setDescripcionDoc(String descripcionDoc) {
		this.descripcionDoc = descripcionDoc;
	}

	public String getGlosaDoc() {
		return glosaDoc;
	}

	public void setGlosaDoc(String glosaDoc) {
		this.glosaDoc = glosaDoc;
	}

	public String getNomDocAdjunto() {
		return nomDocAdjunto;
	}

	public void setNomDocAdjunto(String nomDocAdjunto) {
		this.nomDocAdjunto = nomDocAdjunto;
	}
}

package com.sat.sisat.predial.dto;

import java.io.Serializable;
import java.math.BigDecimal;
import java.sql.Timestamp;

public class DatosDeterminacionPredialValoresDTO implements Serializable {

	private static final long serialVersionUID = 4157750215325392173L;

	private int determinacionId;
	private int determinacionAnio;
	private int personaId;
	private int conceptoId;
	private int subconceptoId;
	private BigDecimal baseImponible;
	private BigDecimal impuesto;
	private String estado;
	private Integer usuario;
	private Timestamp fechaRegistro;


	public DatosDeterminacionPredialValoresDTO() {
	}


	public int getDeterminacionId() {
		return determinacionId;
	}


	public void setDeterminacionId(int determinacionId) {
		this.determinacionId = determinacionId;
	}


	public int getDeterminacionAnio() {
		return determinacionAnio;
	}


	public void setDeterminacionAnio(int determinacionAnio) {
		this.determinacionAnio = determinacionAnio;
	}


	public int getPersonaId() {
		return personaId;
	}


	public void setPersonaId(int personaId) {
		this.personaId = personaId;
	}


	public int getConceptoId() {
		return conceptoId;
	}


	public void setConceptoId(int conceptoId) {
		this.conceptoId = conceptoId;
	}


	public int getSubconceptoId() {
		return subconceptoId;
	}


	public void setSubconceptoId(int subconceptoId) {
		this.subconceptoId = subconceptoId;
	}


	public BigDecimal getBaseImponible() {
		return baseImponible;
	}


	public void setBaseImponible(BigDecimal baseImponible) {
		this.baseImponible = baseImponible;
	}


	public BigDecimal getImpuesto() {
		return impuesto;
	}


	public void setImpuesto(BigDecimal impuesto) {
		this.impuesto = impuesto;
	}


	public String getEstado() {
		return estado;
	}


	public void setEstado(String estado) {
		this.estado = estado;
	}


	public Integer getUsuario() {
		return usuario;
	}


	public void setUsuario(Integer usuario) {
		this.usuario = usuario;
	}


	public Timestamp getFechaRegistro() {
		return fechaRegistro;
	}


	public void setFechaRegistro(Timestamp fechaRegistro) {
		this.fechaRegistro = fechaRegistro;
	}




}

package com.sat.sisat.common.util;

import java.io.File;
import java.io.Serializable;

/**
 * 
 * @author Miguel Macias
 * @version 0.1
 * @since 29/07/2012
 * Clase usada para representar un archivo subido al servidor, este contiene una propiedad
 * file de tipo {@link File}, que tiene apuntado al archivo original en el sistema de archivos.
*/
 
public class FileUpload implements Serializable {

	private static final long serialVersionUID = 6389257537980467890L;
	
	private Integer contentId;
	private File file;
	private String fileName;
	private String contentType;
    private int fileSize;
    
	public File getFile() {
		return file;
	}
	public void setFile(File file) {
		this.file = file;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getContentType() {
		return contentType;
	}
	public void setContentType(String contentType) {
		this.contentType = contentType;
	}
	public int getFileSize() {
		return fileSize;
	}
	public void setFileSize(int fileSize) {
		this.fileSize = fileSize;
	}
	public Integer getContentId() {
		return contentId;
	}
	public void setContentId(Integer contentId) {
		this.contentId = contentId;
	}
}

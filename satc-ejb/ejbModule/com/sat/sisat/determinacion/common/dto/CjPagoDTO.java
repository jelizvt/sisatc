package com.sat.sisat.determinacion.common.dto;

import java.io.Serializable;
import java.sql.Timestamp;

public class CjPagoDTO implements Serializable {

	private static final long serialVersionUID = -3047130336830019135L;

	private int pagoId;
	private Timestamp fechaPago;

	public CjPagoDTO() {
	}

	public int getPagoId() {
		return pagoId;
	}

	public void setPagoId(int pagoId) {
		this.pagoId = pagoId;
	}

	public Timestamp getFechaPago() {
		return fechaPago;
	}

	public void setFechaPago(Timestamp fechaPago) {
		this.fechaPago = fechaPago;
	}
}
package com.sat.sisat.notificaciones.dto;

import java.io.Serializable;
import java.sql.Timestamp;

public class NotificacionesDTO implements Serializable {
	private Integer notificacionesId;
	private String nroDocumento;
	private String fileName;
	private Integer estado;
	private Timestamp fechaNotificacion;
	private String tipoActo;
	private Integer personaId;
	private Long contenId;
	
	
	public Integer getNotificacionesId() {
		return notificacionesId;
	}
	public void setNotificacionesId(Integer notificacionesId) {
		this.notificacionesId = notificacionesId;
	}
	public String getNroDocumento() {
		return nroDocumento;
	}
	public void setNroDocumento(String nroDocumento) {
		this.nroDocumento = nroDocumento;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public Integer getEstado() {
		return estado;
	}
	public void setEstado(Integer estado) {
		this.estado = estado;
	}
	public Timestamp getFechaNotificacion() {
		return fechaNotificacion;
	}
	public void setFechaNotificacion(Timestamp fechaNotificacion) {
		this.fechaNotificacion = fechaNotificacion;
	}
	public String getTipoActo() {
		return tipoActo;
	}
	public void setTipoActo(String tipoActo) {
		this.tipoActo = tipoActo;
	}
	public Integer getPersonaId() {
		return personaId;
	}
	public void setPersonaId(Integer personaId) {
		this.personaId = personaId;
	}
	public Long getContenId() {
		return contenId;
	}
	public void setContenId(Long contenId) {
		this.contenId = contenId;
	}
	
}

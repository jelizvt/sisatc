package com.sat.sisat.alfresco.exception;

public class AlfrescoRepositoryException extends Exception {
	
	private static final long serialVersionUID = 6457017804907669401L;

	public AlfrescoRepositoryException() {
		super();
		
	}

	public AlfrescoRepositoryException(String arg0, Throwable arg1) {
		super(arg0, arg1);
		
	}

	public AlfrescoRepositoryException(String arg0) {
		super(arg0);
		
	}

	public AlfrescoRepositoryException(Throwable arg0) {
		super(arg0);
		
	}
	
}

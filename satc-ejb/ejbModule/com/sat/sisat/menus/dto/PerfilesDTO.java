package com.sat.sisat.menus.dto;
//-=CRAMIREZ=-
import java.io.Serializable;

public class PerfilesDTO implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private int perfilId;
	
	private String nombre;

	private Boolean estado;
	
	private Boolean superfisorCaja;
	
	private Boolean cajero;

	public int getPerfilId() {
		return perfilId;
	}

	public void setPerfilId(int perfilId) {
		this.perfilId = perfilId;
	}

	public String getNombre() {
		return nombre;
	}

	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	public Boolean getEstado() {
		return estado;
	}

	public void setEstado(Boolean estado) {
		this.estado = estado;
	}

	public Boolean getSuperfisorCaja() {
		return superfisorCaja;
	}

	public void setSuperfisorCaja(Boolean superfisorCaja) {
		this.superfisorCaja = superfisorCaja;
	}

	public Boolean getCajero() {
		return cajero;
	}

	public void setCajero(Boolean cajero) {
		this.cajero = cajero;
	}

}
